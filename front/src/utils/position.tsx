import * as THREE from 'three';

const getDecoPoisition = (n: number): THREE.Vector3 => {
  const positions = [
    [0, 0],
    [1.5, -0.5],
    [0.5, 1.5],
    [-1.5, 0.5],
    [-0.5, -1.5],
    [1.5, 0.5], // 5
    [-0.5, 1.5],
    [-1.5, -0.5],
    [0.5, -1.5],
    [1.5, -1.5],
    [1.5, 1.5], // 10
    [-1.5, 1.5],
    [-1.5, -1.5],
    [2.5, -0.5],
    [0.5, 2.5],
    [-2.5, 0.5], // 15
    [-0.5, -2.5],
    [2.5, 0.5],
    [-0.5, 2.5],
    [-2.5, -0.5],
    [0.5, -2.5], // 20
    [2.5, -1.5],
    [1.5, 2.5],
    [-2.5, 1.5],
    [-1.5, -2.5],
    [2.5, 1.5], // 25
    [-1.5, 2.5],
    [-2.5, -1.5],
    [1.5, -2.5],
    [2.5, -2.5],
    [2.5, 2.5]
  ];
  return new THREE.Vector3(positions[n][0], 0, positions[n][1]);
};

export default getDecoPoisition;
